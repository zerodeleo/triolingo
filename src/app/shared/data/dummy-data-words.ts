import { IWords } from '../interface';
import { TLanguage, TWordMap } from '../types';

const getWords = (category: string, languages: TLanguage[]) => {
  const words = WORDS_CATEGORIES.find((word) => word.id === category)
    ?.words.map((item) =>
      languages.reduce((acc, lang) => {
        if (item![lang]) {
          //@ts-ignore
          acc[lang] = item[lang];
        }
        return acc;
      }, {})
    )
    .filter((item) => Object.values(item).some(Boolean)) as TWordMap[];
  return Promise.resolve(words || []);
};

const getAllCategories = () => {
  return Promise.resolve(WORDS_CATEGORIES.map((item) => item.id));
};

